	dec waitcnt
	bne xit
	jmp gon
xit
	jmp fine
	
gon

	lda #2			; Speed
	sta waitcnt
	
	;jmp fine		; Overrides charset scrol renove at will...
	
	lda dir
	bne down
	jmp up

	; Scroll down

down
