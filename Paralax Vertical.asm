	sta waitcnt
	
	lda #15
	sta clocks
	sta $d405
	
	lda #0			; Scroll dir= down
	sta dir
	
	lda #160
	sta counter
	
	ldy #<scroll	; Immediate VBI, lets go!
	ldx #>scroll
	lda #6
	jsr $e45c
	
