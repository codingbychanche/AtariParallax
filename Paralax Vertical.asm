	sta c1
	
	lda c2+6
	sta c2+7
	
	lda c2+5
	sta c2+6
	
	lda c2+4
	sta c2+5
	
	lda c2+3
	sta c2+4
	
	lda c2+2
	sta c2+3
	
	lda c2+1
	sta c2+2
	
	lda c2
	sta c2+1

	sty c2

	jmp fine	; End of down

up
	; Scroll up
	; --------c1

	ldy c1
	
	lda c1+1
	sta c1
	
	lda c1+2
	sta c1+1
	
	lda c1+3
	sta c1+2
	
	lda c1+4
	sta c1+3
	
	lda c1+5
	sta c1+4
	
	lda c1+6
	sta c1+5
	
	lda c1+7
	sta c1+6
	
	lda c1+8
	sta c1+7
	
	;-----c2

	lda c2+1
	sta c2
	
	lda c2+2
	sta c2+1
	
	lda c2+3
	sta c2+2
	
	lda c2+4
	sta c2+3
	
	lda c2+5
	sta c2+4
	
	lda c2+6
	sta c2+5
	
	lda c2+7
	sta c2+6
	
	sty c2+7
	
	; Fine scroll

fine
	lda clocks
	beq hard
	sec
	sbc #1
	sta clocks
	sta $d405
	jmp leave
hard
	lda #15 
	sta clocks
	sta $d405
	
	dec counter
	lda counter
	bne goon
	lda #180
	sta counter
	lda #<scr3end
	sta lms
	lda #>scr3end
	sta lms+1

goon	
	lda lms
	sec
	sbc #20
	sta lms
	lda lms+1
	sbc #0
	sta lms+1
	jmp $e45f		; Leave VBI
	
leave
	jmp $e45f		; Leave VBI

;
; DLI
;


dli
	pha			;  Save registers
	txa
	pha
	tya
	pha
	
	
	pla			; Get registers back
	tay
	pla
	tax
	pla
	
	rti

	
;
; Wait
; Sometimes our code is to fast........
;

